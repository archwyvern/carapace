// Inlines src/backends/webgl/libs/*.glsl into a TS string map so the GLSL stdlib
// bodies are importable across node / tsc / Vite without bundler-specific magic.
// Run via `npm run build:libs` whenever the .glsl libs change.
import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const libsDir = join(here, '..', 'src', 'backends', 'webgl', 'libs');
const outFile = join(here, '..', 'src', 'backends', 'webgl', 'libs.generated.ts');

const files = readdirSync(libsDir).filter((f) => f.endsWith('.glsl')).sort();
const entries = files.map((f) => {
  const name = f.replace(/\.glsl$/, '');
  const body = readFileSync(join(libsDir, f), 'utf8');
  return `  ${JSON.stringify(name)}: ${JSON.stringify(body)},`;
});

const content = `// AUTO-GENERATED by scripts/generate-glsl-libs.mjs -- do not edit by hand.
// Source: src/backends/webgl/libs/*.glsl. Regenerate with \`npm run build:libs\`.
export const GLSL_LIBRARIES: Record<string, string> = {
${entries.join('\n')}
};
`;

writeFileSync(outFile, content);
console.log(`Wrote ${outFile} with ${files.length} libs: ${files.map((f) => f.replace(/\.glsl$/, '')).join(', ')}`);
