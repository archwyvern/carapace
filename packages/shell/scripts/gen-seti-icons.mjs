// Generates the Seti file-icon dataset consumed by the shell's FileIconRegistry.
//
// Seti UI icons (jesseweed/seti-ui, MIT) ship with VSCode as the `theme-seti`
// extension. VSCode reaches a chunk of those icons through a `languageIds` layer
// (langId -> icon), which only pays off if you can resolve a file's language —
// which VSCode does from its builtin language contributions. We have no language
// service at runtime, so we flatten that layer here at build time: every Seti
// languageId entry is expanded into the file extensions / filenames VSCode
// associates with that language, then merged under Seti's explicit ext/name maps
// (explicit always wins). The result is a plain ext/name -> icon lookup.
//
// Regenerate:  node scripts/gen-seti-icons.mjs [path-to-vscode-checkout]
// Default checkout: ~/Projects/thirdparty/vscode

import { readFileSync, writeFileSync, copyFileSync, readdirSync, existsSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { homedir } from "node:os";

const HERE = dirname(fileURLToPath(import.meta.url));
const OUT_DIR = join(HERE, "..", "src", "tree", "fileIcons");
const VSCODE = process.argv[2] ?? join(homedir(), "Projects", "thirdparty", "vscode");

const themePath = join(VSCODE, "extensions", "theme-seti", "icons", "vs-seti-icon-theme.json");
const fontPath = join(VSCODE, "extensions", "theme-seti", "icons", "seti.woff");
if (!existsSync(themePath)) {
  console.error(`Seti theme not found at ${themePath}. Pass a VSCode checkout path as argv[1].`);
  process.exit(1);
}

const theme = JSON.parse(readFileSync(themePath, "utf8"));

// langId -> { extensions:Set<ext-no-dot>, filenames:Set<lowercased> } from builtin language contributions.
const langMap = new Map();
const addLang = (id, exts, names) => {
  let e = langMap.get(id);
  if (!e) langMap.set(id, (e = { extensions: new Set(), filenames: new Set() }));
  for (const x of exts ?? []) e.extensions.add(x.replace(/^\./, "").toLowerCase());
  for (const n of names ?? []) e.filenames.add(String(n).toLowerCase());
};
for (const entry of readdirSync(join(VSCODE, "extensions"), { withFileTypes: true })) {
  if (!entry.isDirectory()) continue;
  const pkgPath = join(VSCODE, "extensions", entry.name, "package.json");
  if (!existsSync(pkgPath)) continue;
  try {
    const pkg = JSON.parse(readFileSync(pkgPath, "utf8"));
    for (const lang of pkg.contributes?.languages ?? []) {
      if (lang.id) addLang(lang.id, lang.extensions, lang.filenames);
    }
  } catch {
    // A non-strict-JSON or unreadable package.json — skip; it just won't contribute languages.
  }
}

// "\\E001" (the raw string the JSON yields is `\E001`) -> 0xE001
const codeOf = (fontCharacter) => parseInt(fontCharacter.replace(/^\\/, ""), 16);

// iconDefinitions: drop the *_light variants (light theme only — we ship dark), keep code+color.
const iconDefinitions = {};
for (const [id, def] of Object.entries(theme.iconDefinitions)) {
  if (id.endsWith("_light")) continue;
  iconDefinitions[id] = { code: codeOf(def.fontCharacter), ...(def.fontColor ? { color: def.fontColor } : {}) };
}

// Explicit Seti maps are authoritative; the flattened language layer only fills gaps.
const byExtension = {};
const byFileName = {};
for (const [ext, defId] of Object.entries(theme.fileExtensions ?? {})) byExtension[ext.toLowerCase()] = defId;
for (const [name, defId] of Object.entries(theme.fileNames ?? {})) byFileName[name.toLowerCase()] = defId;

let langExt = 0;
let langName = 0;
for (const [langId, defId] of Object.entries(theme.languageIds ?? {})) {
  const lang = langMap.get(langId);
  if (!lang) continue;
  for (const ext of lang.extensions) {
    if (byExtension[ext] === undefined) { byExtension[ext] = defId; langExt++; }
  }
  for (const name of lang.filenames) {
    if (byFileName[name] === undefined) { byFileName[name] = defId; langName++; }
  }
}

// Drop any mapping that points at a definition we didn't keep (defensive).
for (const map of [byExtension, byFileName]) {
  for (const [k, defId] of Object.entries(map)) {
    if (!iconDefinitions[defId]) delete map[k];
  }
}

const sortObj = (o) => Object.fromEntries(Object.entries(o).sort(([a], [b]) => a.localeCompare(b)));
const stringify = (o) => JSON.stringify(sortObj(o), null, 2);

const banner = `// AUTO-GENERATED by scripts/gen-seti-icons.mjs — do not edit by hand.
// Source: Seti UI (jesseweed/seti-ui, MIT) via VSCode theme-seti; the languageIds
// layer is flattened into extensions/filenames using VSCode's builtin language
// contributions. Regenerate: node scripts/gen-seti-icons.mjs [vscode-checkout].
`;

const out = `${banner}
/** One Seti glyph: a codepoint into the bundled \`seti.woff\` font, plus its colour. */
export interface SetiIconDef {
  code: number;
  color?: string;
}

export const setiIconDefinitions: Record<string, SetiIconDef> = ${stringify(iconDefinitions)};

/** Lowercased extension (no dot) -> icon definition id. Longest extension wins at lookup. */
export const setiByExtension: Record<string, string> = ${stringify(byExtension)};

/** Lowercased full filename -> icon definition id. Beats extension matches. */
export const setiByFileName: Record<string, string> = ${stringify(byFileName)};

/** Fallback icon definition id for files matching nothing above. */
export const setiDefaultFile = ${JSON.stringify(theme.file)};
`;

copyFileSync(fontPath, join(OUT_DIR, "seti.woff"));
writeFileSync(join(OUT_DIR, "setiIconData.ts"), out);

console.log(
  `Wrote setiIconData.ts: ${Object.keys(iconDefinitions).length} defs, ` +
    `${Object.keys(byExtension).length} extensions (${langExt} from language layer), ` +
    `${Object.keys(byFileName).length} filenames (${langName} from language layer).`,
);
